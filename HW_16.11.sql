4. Дайте короткий ответ на вопрос: что такое проекция в БД и зачем она нужна?

Проекция означает выборку конкретных определенных нужных полей из таблицы,  а не всех полей, ведь их может быть очень много и не все они нужны для выборки.
 Проекция нужна для того, чтобы уменьшить объем пересылаемых данных при запросе из БД, т. е. идет оптимизация путем уменьшения времени ответа.


